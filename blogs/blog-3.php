                    <p>Let’s be honest – selling for a mid-market B2B company isn’t for the faint of heart. You’re
                        caught in this weird
                        middle ground where you need to grow fast but don’t have the fancy tools and resources that the
                        big players do.
                        I recently spoke with Sarah Chen, a sales director at a growing SaaS company, who put it
